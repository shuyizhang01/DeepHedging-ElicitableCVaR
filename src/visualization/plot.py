# 4. Scoring curvature  — generates its own synthetic data, no paths needed
# ---------------------------------------------------------------------------

def plot_scoring_curvature(
    score_fn,
    save_dir:  str   = ".",
    N:         int   = 10_000,
    half:      float = 2.0,
    alpha_ds:  float = 0.95,
) -> None:
    plt.rcParams.update({"font.family": "serif", "mathtext.fontset": "cm"})

    np.random.seed(42)
    colors = ["#378ADD", "#D85A30", "#1D9E75", "#7F77DD", "#BA7517", "#D4537E"]

    def _score_np(a1, a2, y, alpha, C):
        return score_fn(
            torch.tensor(a1, dtype=torch.float32),
            torch.tensor(a2, dtype=torch.float32),
            torch.tensor(y,  dtype=torch.float32),
            alpha, C,
        ).numpy()

    alphas = [0.10, 0.30, 0.50, 0.70, 0.85, 0.95]
    y_base = np.random.normal(0, 1, N)
    C_base = abs(y_base.min())

    target_var = np.quantile(np.random.normal(0, 1, N), alpha_ds)
    fixed_var_datasets = {}
    for sigma in [0.5, 0.8, 1.0, 1.5, 2.0, 3.0]:
        y  = np.random.normal(0, sigma, N)
        y += target_var - np.quantile(y, alpha_ds)
        ev = np.quantile(y, alpha_ds)
        ec = y[y >= ev].mean()
        fixed_var_datasets[f"CVaR={ec:.2f}"] = (y, ev, ec)

    target_cvar = 1.8
    fixed_cvar_datasets = {}
    for shift in [-2.5, -1.5, -0.5, 0.5, 1.5, 2.5]:
        y      = np.random.normal(shift, 1, N)
        ev     = np.quantile(y, alpha_ds)
        ec_raw = y[y >= ev].mean()
        mask   = y >= ev
        y[mask] = ev + (y[mask] - ev) * (target_cvar - ev) / max(ec_raw - ev, 1e-6)
        ev = np.quantile(y, alpha_ds)
        ec = y[y >= ev].mean()
        fixed_cvar_datasets[f"VaR={ev:.2f}"] = (y, ev, ec)

    y1 = np.random.normal(5,  1, N)
    y2 = np.random.normal(25, 1, N)

    fig, axes = plt.subplots(3, 2, figsize=(13, 13))

    for alpha, color in zip(alphas, colors):
        ev = np.quantile(y_base, alpha)
        ec = y_base[y_base >= ev].mean()

        grid = np.linspace(ev - half, ev + half, 300)
        sc   = np.array([_score_np(np.full(N, a), np.full(N, a), y_base, alpha, C_base).mean() for a in grid])
        oi   = np.argmin(sc)
        axes[0, 0].plot(grid, sc, color=color, lw=1.6, alpha=0.85, label=f"alpha={alpha:.2f}")
        axes[0, 0].scatter(grid[oi], sc[oi], color=color, s=60, zorder=5)

        grid = np.linspace(ec - half, ec + half, 300)
        sc   = np.array([_score_np(np.full(N, ev), np.full(N, a), y_base, alpha, C_base).mean() for a in grid])
        oi   = np.argmin(sc)
        axes[0, 1].plot(grid, sc, color=color, lw=1.6, alpha=0.85, label=f"alpha={alpha:.2f}")
        axes[0, 1].scatter(grid[oi], sc[oi], color=color, s=60, zorder=5)

    for (name, (y, ev, ec)), color in zip(fixed_var_datasets.items(), colors):
        C    = abs(y.min())
        grid = np.linspace(ev - half, ev + half, 300)
        sc   = np.array([_score_np(np.full(N, a), np.full(N, a), y, alpha_ds, C).mean() for a in grid])
        oi   = np.argmin(sc)
        axes[1, 0].plot(grid, sc, color=color, lw=1.6, alpha=0.85, label=name)
        axes[1, 0].scatter(grid[oi], sc[oi], color=color, s=60, zorder=5)

    for (name, (y, ev, ec)), color in zip(fixed_cvar_datasets.items(), colors):
        C    = abs(y.min())
        grid = np.linspace(ec - half, ec + half, 300)
        sc   = np.array([_score_np(np.full(N, ev), np.full(N, a), y, alpha_ds, C).mean() for a in grid])
        oi   = np.argmin(sc)
        axes[1, 1].plot(grid, sc, color=color, lw=1.6, alpha=0.85, label=name)
        axes[1, 1].scatter(grid[oi], sc[oi], color=color, s=60, zorder=5)

    rel_grid = np.linspace(-3.5, 3.5, 300)
    for y_samp, color, label in [
        (y1, "#378ADD", r"$\mathcal{N}(5,1)$"),
        (y2, "#D85A30", r"$\mathcal{N}(25,1)$"),
    ]:
        C  = abs(y_samp.min())
        ev = np.quantile(y_samp, alpha_ds)
        ec = y_samp[y_samp >= ev].mean()

        sc = np.array([_score_np(np.full(N, ev + a), np.full(N, ev + a), y_samp, alpha_ds, C).mean() for a in rel_grid])
        sc -= sc.min()
        oi  = np.argmin(sc)
        axes[2, 0].plot(rel_grid, sc, color=color, lw=1.6, alpha=0.85, label=label)
        axes[2, 0].scatter(rel_grid[oi], sc[oi], color=color, s=60, zorder=5)

        sc = np.array([_score_np(np.full(N, ev), np.full(N, ec + a), y_samp, alpha_ds, C).mean() for a in rel_grid])
        sc -= sc.min()
        oi  = np.argmin(sc)
        axes[2, 1].plot(rel_grid, sc, color=color, lw=1.6, alpha=0.85, label=label)
        axes[2, 1].scatter(rel_grid[oi], sc[oi], color=color, s=60, zorder=5)

    axes[2, 0].axvline(0, color="k", lw=0.8, linestyle="--", alpha=0.4)
    axes[2, 1].axvline(0, color="k", lw=0.8, linestyle="--", alpha=0.4)

    panel_xlabels = [
        (axes[0, 0], r"$\mathfrak{a}_1$"), (axes[0, 1], r"$\mathfrak{a}_2$"),
        (axes[1, 0], r"$\mathfrak{a}_1$"), (axes[1, 1], r"$\mathfrak{a}_2$"),
        (axes[2, 0], r"$\mathfrak{a}_1$"), (axes[2, 1], r"$\mathfrak{a}_2$"),
    ]
    for ax, xlabel in panel_xlabels:
        ax.set_xlabel(xlabel, fontsize=13, color="#444444")
        ax.set_ylabel(r"$\mathrm{s}(\mathfrak{a}_1,\mathfrak{a}_2)$", fontsize=13, color="#444444")
        _style_ax(ax)
        ax.tick_params(labelbottom=False, labelleft=False)
        ax.axhline(0, color="k", lw=0.5, alpha=0.3)
        ydata  = np.concatenate([l.get_ydata() for l in ax.lines])
        finite = ydata[np.isfinite(ydata)]
        p5, p95 = np.percentile(finite, [5, 95])
        margin  = (p95 - p5) * 0.4
        ax.set_ylim(p5 - margin, p95 + margin)

    alpha_handles = [mlines.Line2D([], [], color=c, lw=2, label=f"alpha={a:.2f}") for a, c in zip(alphas, colors)]
    axes[0, 0].legend(handles=alpha_handles, fontsize=8, frameon=False)
    axes[0, 1].legend(handles=alpha_handles, fontsize=8, frameon=False)

    for ax, ds in [(axes[1, 0], fixed_var_datasets), (axes[1, 1], fixed_cvar_datasets)]:
        handles = [mlines.Line2D([], [], color=c, lw=2, label=n) for (n, _), c in zip(ds.items(), colors)]
        ax.legend(handles=handles, fontsize=8, frameon=False)

    for ax in [axes[2, 0], axes[2, 1]]:
        ax.legend(fontsize=9, frameon=False)

    plt.tight_layout()
    _save(fig, save_dir, "scoring_curvature_full")
    plt.show()


