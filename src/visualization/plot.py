def plot_dynamic_risk_comparison(
    alpha_label:             str = "alpha95",
    data_dir:                str = "content/data",
    all_models:              dict = None,
    all_norms:               dict = None,
    env                          = None,
    CriticVaRExcess              = None,
    CriticCVaR                   = None,
    nested_ckpt_static_path: str = "NestedCritics/critic_checkpoint_static_epoch4.pt",
    nested_ckpt_dynamic_path:str = "NestedCritics/critic_checkpoint_epoch4.pt",
    save_dir:                str = ".",
    n_paths:                 int = 1000,
    risk_threshold:          int = 95,
    n_groups:                int = 18,
) -> None:
    plt.rcParams.update({"font.family": "serif", "mathtext.fontset": "cm"})

    device     = env.device
    T          = env.T_days
    group_size = T // n_groups
    colors     = plt.cm.tab10(np.linspace(0, 0.6, 6))

    scoring_display = {
        "rational": "Rational", "arcsinh": "Arcsinh", "log": "Log",
        "arcsin":   "Arcsin",   "arctan":  "Arctan",  "power03": "Power",
    }

    def _load_nested(ckpt_path):
        ckpt    = torch.load(ckpt_path, map_location="cpu", weights_only=False)
        critics = torch.nn.ModuleList([
            CriticCVaR(env.state_dim, group_size, hidden_dim=64, head_dim=64, device=device)
            for _ in range(n_groups)
        ])
        for g in range(n_groups):
            critics[g].load_state_dict(ckpt["critics"][g])
            critics[g].to(device).eval()
        return critics

    nested_critics_static  = _load_nested(nested_ckpt_static_path)
    nested_critics_dynamic = _load_nested(nested_ckpt_dynamic_path)

    fig, ax = plt.subplots(figsize=(13, 4.5))
    t_axis  = np.arange(T) / 252

    # ── Dynamic CVaR from RL agent critics (CriticVaRExcess) ──────────────
    for (sk, label), color in zip(scoring_display.items(), colors):
        states_np = _load(data_dir, alpha_label, sk, "states.npy")[:, :n_paths, :]
        costs_np  = _load(data_dir, alpha_label, sk, "costs.npy")[:, :n_paths]

        raw_critics = all_models[alpha_label][sk]["critics"]
        critics = []
        for c in raw_critics:
            fresh = CriticVaRExcess(
                env.state_dim, group_size,
                head_dim_var=128, head_dim_excess=128,
            )
            fresh.load_state_dict(c.state_dict())
            fresh.to(device).eval()
            critics.append(fresh)

        b_values = all_norms[alpha_label][sk]["b_values"]
        states_t = torch.tensor(states_np, dtype=torch.float32, device=device)

        cvar_grid = np.zeros((n_paths, T))
        with torch.no_grad():
            for g in range(n_groups):
                gs, ge = g * group_size, (g + 1) * group_size
                v, e = critics[g](states_t[gs:ge])
                cvar_grid[:, gs:ge] = (
                    (v + e).squeeze(-1) + b_values[n_groups - 1 - g]
                ).T.cpu().numpy()

        ax.plot(t_axis, cvar_grid.mean(axis=0),
                label=label, color=color, linewidth=1.4, alpha=0.75, zorder=3)

    # ── Nested heuristic CVaR for static actor ─────────────────────────────
    states_s_np = _load(data_dir, alpha_label, "static", "states.npy")[:, :n_paths, :]
    states_s    = torch.tensor(states_s_np, dtype=torch.float32, device=device)

    nested_s = np.zeros((n_paths, T))
    with torch.no_grad():
        for g in range(n_groups):
            gs, ge = g * group_size, (g + 1) * group_size
            nested_s[:, gs:ge] = nested_critics_static[g](states_s[gs:ge]).T.cpu().numpy()

    ax.plot(t_axis, nested_s.mean(axis=0),
            color="black", linewidth=1.4, linestyle="--", zorder=2,
            label="Nested - Static")

    # ── Nested heuristic CVaR for power03 (dynamic) actor ─────────────────
    states_d_np = _load(data_dir, alpha_label, "power03", "states.npy")[:, :n_paths, :]
    states_d    = torch.tensor(states_d_np, dtype=torch.float32, device=device)

    nested_d = np.zeros((n_paths, T))
    with torch.no_grad():
        for g in range(n_groups):
            gs, ge = g * group_size, (g + 1) * group_size
            nested_d[:, gs:ge] = nested_critics_dynamic[g](states_d[gs:ge]).T.cpu().numpy()

    ax.plot(t_axis, nested_d.mean(axis=0),
            color=colors[5], linewidth=1.4, linestyle="--", zorder=2,
            label="Nested - Power")

    # ── Legend ─────────────────────────────────────────────────────────────
    from matplotlib.lines import Line2D as _L2D
    leg1 = ax.legend(
        handles=ax.lines[:6], labels=list(scoring_display.values()),
        title="Scoring Function | Dynamic Risk Model",
        frameon=False, ncol=1, loc="upper right",
    )
    ax.add_artist(leg1)
    extra_handles = [
        _L2D([0], [0], color="black",   lw=1.4, linestyle="--"),
        _L2D([0], [0], color=colors[5], lw=1.4, linestyle="--"),
    ]
    ax.legend(
        handles=extra_handles,
        labels=["Nested CVaR - Static actor", "Nested CVaR - Power actor"],
        frameon=False, loc="upper right", bbox_to_anchor=(1, 0.55),
    )

    ax.set_xlabel("Time (years)")
    ax.set_ylabel("Dynamic CVaR Risk")
    _style_ax(ax)

    plt.tight_layout()
    _save(fig, save_dir, "DynamicRiskComparison")
    plt.show()

