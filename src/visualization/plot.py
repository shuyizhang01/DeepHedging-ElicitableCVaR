def plot_extreme_trajectory(
    alpha_label:    str = "alpha95",
    data_dir:       str = "content/data",
    env                 = None,
    save_dir:       str = ".",
    n_paths:        int = 1000,
) -> None:
    plt.rcParams.update({"font.family": "serif", "mathtext.fontset": "cm"})

    T          = env.T_days
    timesteps  = np.arange(T)     / 252
    t_full     = np.arange(T + 1) / 252

    basket_weights = np.array([0.07690456, 0.44895452, 0.26472574, 0.20941517])
    corr_pairs  = [(i, j) for i in range(4) for j in range(i + 1, 4)]
    corr_labels = [f"{ASSET_NAMES[i]}-{ASSET_NAMES[j]}" for i, j in corr_pairs]

    colors_scoring = [
        _SCORING_COLORS[SCORING_KEYS.index(sk)] if sk in SCORING_KEYS else "#888888"
        for sk in SCORING_DISPLAY
    ]
    colors_assets = list(plt.cm.Set1(np.linspace(0, 0.8, 4)))
    colors_corr   = list(plt.cm.tab10(np.linspace(0, 1.0, 6)))

    first_sk = SCORING_KEYS[0]
    S_paths  = _load(data_dir, alpha_label, first_sk, "S_paths.npy")[:n_paths]   # [n, T+1, 4]

    basket_np  = (S_paths * basket_weights).sum(axis=-1)                          # [n, T+1]

    # Find the path with the largest total price run-up (terminal / initial)
    runup     = basket_np[:, -1] / basket_np[:, 0]
    hard_idx  = int(np.argmax(runup))
    print(f"Hard trajectory: idx={hard_idx}  run-up={runup[hard_idx]:.4f}x  "
          f"(basket start={basket_np[hard_idx, 0]:.2f}  end={basket_np[hard_idx, -1]:.2f})")

    basket_hard = basket_np[hard_idx]
    S_hard      = S_paths[hard_idx]                                               # [T+1, 4]

    h_paths  = _load(data_dir, alpha_label, first_sk, "h_paths.npy")[:n_paths]   # [n, T+1, 4]
    h_hard   = h_paths[hard_idx]                                                  # [T+1, 4]
    vol_hard = np.sqrt(252 * h_hard)

    Q_paths  = _load(data_dir, alpha_label, first_sk, "Q_paths.npy")[:n_paths]   # [n, T+1, 4, 4]
    Q_hard   = Q_paths[hard_idx]                                                  # [T+1, 4, 4]
    d_diag   = np.sqrt(np.diagonal(Q_hard, axis1=1, axis2=2))
    R_hard   = Q_hard / (d_diag[:, :, None] * d_diag[:, None, :])
    corr_hard = np.stack([R_hard[:, i, j] for i, j in corr_pairs], axis=1)       # [T+1, 6]

    all_actions  = {}
    terminal_pnl = {}

    for sk in SCORING_KEYS:
        actions = _load(data_dir, alpha_label, sk, "actions.npy")[:, :n_paths, :]       # [T, n, 4]
        portval = _load(data_dir, alpha_label, sk, "portfolio_values.npy")[:n_paths, :]  # [n, T]
        all_actions[sk]  = actions[:, hard_idx, :]                                       # [T, 4]
        terminal_pnl[sk] = float(portval[hard_idx, -1])

    actions_s = _load(data_dir, alpha_label, "static", "actions.npy")[:, :n_paths, :]
    portval_s = _load(data_dir, alpha_label, "static", "portfolio_values.npy")[:n_paths, :]
    all_actions["static"]  = actions_s[:, hard_idx, :]
    terminal_pnl["static"] = float(portval_s[hard_idx, -1])

    # ── 4×2 layout ────────────────────────────────────────────────────────
    # Rows 0–1 (top 2×2): one asset per panel, positions for all actors
    # Rows 2–3 (bottom 2×2): basket | prices | volatilities | correlations
    fig, axes = plt.subplots(4, 2, figsize=(14, 18))

    asset_axes = [axes[0, 0], axes[0, 1], axes[1, 0], axes[1, 1]]
    lower_axes = [axes[2, 0], axes[2, 1], axes[3, 0], axes[3, 1]]

    # ── Top 2×2: per-asset positions ──────────────────────────────────────
    for asset_idx, ax in enumerate(asset_axes):
        for (sk, label), color in zip(SCORING_DISPLAY.items(), colors_scoring):
            ax.plot(timesteps, all_actions[sk][:, asset_idx],
                    color=color, linewidth=1.4, alpha=0.85)
        ax.plot(timesteps, all_actions["static"][:, asset_idx],
                color="black", linewidth=2.2, linestyle="--", alpha=0.95)
        ax.set_title(ASSET_NAMES[asset_idx], fontsize=11, pad=4)
        ax.set_xlabel("Time (Years)", fontsize=9)
        ax.set_ylabel("Position", fontsize=9)
        _style_ax(ax)

    # ── Bottom 2×2: market-state panels ───────────────────────────────────
    # [2,0] Basket value
