def plot_portfolio_delta(
    alpha_label: str = "alpha95",
    data_dir:    str = "content/data",
    env                = None,
    save_dir:    str = ".",
    n_paths:     int = 1000,
) -> None:
    plt.rcParams.update({"font.family": "serif", "mathtext.fontset": "cm"})

    T       = env.T_days
    weights = env.basket_weights.cpu().numpy()

    norm      = Normalize(vmin=0, vmax=T - 1)
    t_indices = np.repeat(np.arange(T), n_paths)

    # Load shared S_paths ONCE — same paths for all actors
    shared_S = np.load(os.path.join(data_dir, alpha_label, "_shared_paths", "S_paths.npy"))
    basket_moneyness = (shared_S[:n_paths, :T, :] * weights).sum(axis=-1) / env.K  # [n, T]
    del shared_S

    fig, axes = plt.subplots(
        2, 3, figsize=(12, 7),
        sharex=True, sharey=True,
        constrained_layout=True,
    )

    for idx, sk in enumerate(SCORING_KEYS):
        ax = axes.flat[idx]

        actions_np = np.load(
            os.path.join(data_dir, alpha_label, "_shared_paths", sk, "actions.npy")
        )[:, :n_paths, :]  # [T, n, 4]
        port_delta = (actions_np * weights).sum(axis=-1)  # [T, n]

        x = basket_moneyness.T.ravel()  # [T, n] -> T*n
        y = port_delta.ravel()          # [T, n] -> T*n

        ax.scatter(x, y, s=0.5, alpha=0.3,
                   c=t_indices, cmap="viridis", norm=norm,
                   rasterized=True, linewidths=0)

        ax.set_title(SCORING_DISPLAY[sk], fontsize=12, color="#444444")
        _style_ax(ax)
        ax.set_axisbelow(True)
        if idx in [0, 3]:
            ax.set_ylabel("Weighted Aggregate Position", fontsize=12, color="#444444")
        if idx in [3, 4, 5]:
            ax.set_xlabel("Moneyness", fontsize=12, color="#444444")

    _save(fig, save_dir, "portfolio_delta_plot")
    plt.show()

